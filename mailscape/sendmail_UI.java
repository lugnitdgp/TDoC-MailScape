    private void jButton3MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton3MouseClicked
        // TODO add your handling code here:
        jLabel6.setText("Loading...");
        jLabel6.paintImmediately(jLabel6.getVisibleRect());
        Get_Mail mail=new Get_Mail();
        data=mail.downloadEmails("imap", "imap.gmail.com", "993", USER, PASS);
        DefaultListModel model=new DefaultListModel();
        for(int i=0; i<data.length; i++)
        {
            if(data[i]==null)
            break;
            jList1.setFixedCellHeight(50);
            jList1.setBackground(new java.awt.Color(255, 255, 255));  
            model.addElement(data[i]);
            jLabel6.setText("Last 30 emails:");
            jLabel6.paintImmediately(jLabel6.getVisibleRect());
        }
        jList1.setModel(model);
    }//GEN-LAST:event_jButton3MouseClicked

